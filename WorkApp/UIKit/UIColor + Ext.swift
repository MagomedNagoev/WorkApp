
import UIKit

extension UIColor {
    
    static func buttonWhite() -> UIColor {
        return #colorLiteral(red: 0.8549019608, green: 0.8980392157, blue: 0.9294117647, alpha: 1)
    }
    
    static func buttonBlue() -> UIColor {
        return #colorLiteral(red: 0.1490196078, green: 0.3529411765, blue: 0.7019607843, alpha: 1)
    }
    
    static func backgroundBlue() -> UIColor {
        return #colorLiteral(red: 0.3764705882, green: 0.6980392157, blue: 1, alpha: 1)
    }
    
    static func buttonRed() -> UIColor {
        return #colorLiteral(red: 0.4818281938, green: 0.1495427971, blue: 0.1473460643, alpha: 1)
    }
        
}
